const app = express();
const PORT = process.env.PORT || 3000;

// ---------- 中间件 ----------

// CORS 允许跨域（本地开发时方便，生产环境同域访问不受影响）
app.use(cors());

// 解析 JSON 请求体
app.use(express.json());

// 限流：每个 IP 每分钟最多 30 次请求
const limiter = rateLimit({
  windowMs: 60 * 1000,  // 1 分钟窗口
  max: 30,              // 最多 30 次
  standardHeaders: true,
  legacyHeaders: false,
  message: { error: '请求太频繁了，请稍后再试' },
});
app.use('/chat', limiter);

// ---------- 静态文件服务 ----------
app.use(express.static(path.join(__dirname, 'public')));

// ---------- API 接口 ----------

// POST /chat — 聊天接口（SSE 流式响应）
app.post('/chat', async (req, res) => {
  const { message } = req.body;

  // 校验用户输入
  if (!message || typeof message !== 'string' || message.trim().length === 0) {
    return res.status(400).json({ error: '消息不能为空' });
  }
  if (message.length > 4000) {
    return res.status(400).json({ error: '消息太长，请控制在 4000 字以内' });
  }

  // 设置 SSE 响应头
  res.setHeader('Content-Type', 'text/event-stream');
  res.setHeader('Cache-Control', 'no-cache');
  res.setHeader('Connection', 'keep-alive');
  res.setHeader('X-Accel-Buffering', 'no');  // 禁用 nginx 缓冲

  try {
    // 调用 DeepSeek API
    const response = await fetch('https://api.deepseek.com/v1/chat/completions', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'Authorization': `Bearer ${DEEPSEEK_API_KEY}`,
      },
      body: JSON.stringify({
        model: 'deepseek-chat',
        messages: [
          { role: 'system', content: SYSTEM_PROMPT },
          { role: 'user', content: message },
        ],
        stream: true,
        temperature: 0.8,
        max_tokens: 2048,
      }),
      signal: AbortSignal.timeout(60000),  // 60 秒超时
    });

    // API 返回错误时
    if (!response.ok) {
      const errorText = await response.text();
      let errorMessage = 'AI 服务暂时不可用';

      if (response.status === 401) {
        errorMessage = 'API Key 无效，请联系管理员';
      } else if (response.status === 429) {
        errorMessage = 'API 请求频率过高，请稍后再试';
      } else if (response.status === 402) {
        errorMessage = 'API 余额不足，请联系管理员充值';
      }

      console.error(`DeepSeek API 错误 (${response.status}): ${errorText}`);
      res.write(`data: ${JSON.stringify({ error: errorMessage })}\n\n`);
      res.write('data: [DONE]\n\n');
      res.end();
      return;
    }

    // 逐块读取流式响应，原样转发给前端
    const reader = response.body.getReader();
    const decoder = new TextDecoder();
    let buffer = '';

    while (true) {
      const { done, value } = await reader.read();
      if (done) break;

      buffer += decoder.decode(value, { stream: true });
      const lines = buffer.split('\n');
      // 最后一个可能不完整，留到下次处理
      buffer = lines.pop() || '';

      for (const line of lines) {
        if (line.startsWith('data: ')) {
          // 直接转发 DeepSeek 的 SSE 数据
          res.write(line + '\n\n');
        }
      }
    }

    // 处理缓冲区剩余内容
    if (buffer && buffer.startsWith('data: ')) {
      res.write(buffer + '\n\n');
    }

    // 发送结束标记
    res.write('data: [DONE]\n\n');
    res.end();

  } catch (error) {
    console.error('请求 DeepSeek API 异常:', error.message);

    // 如果响应头还没发送，返回 JSON 错误
    if (!res.headersSent) {
      return res.status(500).json({ error: '服务器内部错误，请稍后再试' });
    }

    // 如果已经开始流式传输，用 SSE 格式发送错误
    if (error.name === 'TimeoutError' || error.name === 'AbortError') {
      res.write(`data: ${JSON.stringify({ error: '请求超时，请稍后再试' })}\n\n`);
    } else {
      res.write(`data: ${JSON.stringify({ error: '服务器内部错误' })}\n\n`);
    }
    res.write('data: [DONE]\n\n');
    res.end();
  }
});

// ---------- 启动服务器 ----------
app.listen(PORT, () => {
  console.log(`小崔 AI 分身 已启动: http://localhost:${PORT}`);
  console.log('按 Ctrl+C 停止服务器');
});
